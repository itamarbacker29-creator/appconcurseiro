# ── Upsert via REST ─────────────────────────────────────────────
def upsert(edital: dict) -> bool:
    payload = {k: v for k, v in edital.items() if v is not None}
    # materias como JSON array
    if "materias" in payload:
        payload["materias"] = payload["materias"]  # já é lista Python — será serializada
    body = json.dumps(payload).encode()
    req = urllib.request.Request(
        f"{SUPABASE_URL}/rest/v1/editais",
        data=body,
        headers={
            "apikey": SUPABASE_KEY,
            "Authorization": f"Bearer {SUPABASE_KEY}",
            "Content-Type": "application/json",
            "Prefer": "resolution=merge-duplicates",
        },
        method="POST",
    )
    try:
        with urllib.request.urlopen(req) as resp:
            return resp.status in (200, 201)
    except urllib.error.HTTPError as e:
        err = e.read().decode()
        print(f"    HTTP {e.code}: {err[:300]}")
        return False

